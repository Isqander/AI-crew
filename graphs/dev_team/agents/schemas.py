# ── Developer ────────────────────────────────────────────────────

class DeveloperFileOutput(BaseModel):
    """A single code file generated by the developer."""
    path: str = Field(description="File path (e.g. src/main.py)")
    language: str = Field(description="Programming language")
    content: str = Field(description="File content")


class DeveloperResponse(BaseModel):
    """Output of the developer's implementation."""
    files: list[DeveloperFileOutput] = Field(
        default_factory=list, description="Generated code files")


# ── Exploration plan ─────────────────────────────────────────────

class ExplorationStep(BaseModel):
    """A single step in the QA exploration plan."""
    name: str = Field(description="Step name")
    action: str = Field(description="What to do")
    expected: str = Field("", description="Expected outcome")


class ExplorationPlanResponse(BaseModel):
    """QA exploration test plan."""
    steps: list[ExplorationStep] = Field(
        default_factory=list, description="Ordered list of exploration steps")


# ── DevOps ──────────────────────────────────────────────────────

class InfraFileOutput(BaseModel):
    """A single infrastructure file generated by the DevOps agent."""
    path: str = Field(description="File path (e.g. Dockerfile, docker-compose.prod.yml)")
    content: str = Field(description="File content")


class DevOpsResponse(BaseModel):
    """Output of the DevOps agent's infrastructure generation."""
    infra_files: list[InfraFileOutput] = Field(
        default_factory=list, description="Generated infrastructure files")
    deploy_url: str = Field("", description="Expected deployment URL")
    env_vars_needed: list[str] = Field(
        default_factory=list, description="Environment variables required on deploy VPS")
    notes: str = Field("", description="Human-readable deployment notes")
